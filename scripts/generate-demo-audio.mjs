import { createHash } from 'node:crypto';
import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repo = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputDir = path.join(repo, 'public', 'audio', 'demos');
const manifestPath = path.join(repo, 'src', 'audio', 'demoVoiceClips.ts');
const referenceDir = path.join(repo, '.cache', 'voice-references');
const modelVersion = '9cfba4c265e685f840612be835424f8c33bdee685d7466ece7684b0d9d4c0b1c';
const voices = new Set(['ANNA', 'SARAH', 'GRACE', 'ELLIOTT']);
const exaggeration = {
  neutral: 0.45,
  happy: 0.7,
  laughing: 0.7,
  surprised: 0.65,
  sad: 0.55,
  angry: 0.55,
  scared: 0.55,
  confused: 0.55,
};

const envText = await readFile(path.join(repo, '..', '.env'), 'utf8');
const token = envText.match(/^REPLICATE_API_(?:TOKEN|KEY)=(.+)$/m)?.[1]?.trim().replace(/^['"]|['"]$/g, '');
if (!token) throw new Error('REPLICATE_API_TOKEN or REPLICATE_API_KEY was not found in Documents/.env.');

const storyFiles = (await readdir(path.join(repo, 'stories'))).filter((name) => name.endsWith('.story')).sort();
const lines = new Map();
for (const storyFile of storyFiles) {
  const source = await readFile(path.join(repo, 'stories', storyFile), 'utf8');
  for (const rawLine of source.split(/\r?\n/u)) {
    const match = rawLine.match(/^([A-Z][A-Z0-9_]*)(?: \(([^)]+)\))?:\s*(.+)$/u);
    if (!match || !voices.has(match[1])) continue;
    const line = { character: match[1], emotion: match[2]?.toLowerCase() ?? 'neutral', text: match[3].trim() };
    const key = `${line.character}\0${line.emotion}\0${line.text}`;
    lines.set(key, line);
  }
}

await mkdir(outputDir, { recursive: true });
const references = new Map();
for (const character of voices) {
  const wav = await readFile(path.join(referenceDir, `${character.toLowerCase()}.wav`));
  references.set(character, `data:audio/wav;base64,${wav.toString('base64')}`);
}

const entries = [...lines.entries()].map(([key, line]) => {
  const hash = createHash('sha256').update(key).digest('hex');
  return { key, line, hash, filename: `${line.character.toLowerCase()}-${hash.slice(0, 20)}.wav` };
});

async function createClip(entry) {
  const destination = path.join(outputDir, entry.filename);
  try {
    await readFile(destination);
    console.log(`cached ${entry.filename}`);
    return;
  } catch {}

  const language = /[\u3400-\u4dbf\u4e00-\u9fff\uf900-\ufaff]/u.test(entry.line.text) ? 'zh' : 'en';
  const response = await fetch('https://api.replicate.com/v1/predictions', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${token}`,
      'Content-Type': 'application/json',
      Prefer: 'wait=60',
    },
    body: JSON.stringify({
      version: modelVersion,
      input: {
        text: entry.line.text,
        language,
        reference_audio: references.get(entry.line.character),
        exaggeration: exaggeration[entry.line.emotion] ?? exaggeration.neutral,
        cfg_weight: language === 'zh' ? 0.3 : 0.45,
        temperature: 0.65,
        seed: Number.parseInt(entry.hash.slice(0, 8), 16) % 2_147_483_647,
      },
    }),
  });
  let prediction = await response.json();
  if (!response.ok) throw new Error(prediction.detail ?? prediction.error ?? `Replicate returned ${response.status}.`);

  for (let attempt = 0; !prediction.output && attempt < 30; attempt += 1) {
    if (prediction.status === 'failed' || prediction.status === 'canceled') {
      throw new Error(prediction.error ?? `Prediction ${prediction.status}.`);
    }
    await new Promise((resolve) => setTimeout(resolve, 2_000));
    const poll = await fetch(prediction.urls.get, { headers: { Authorization: `Bearer ${token}` } });
    prediction = await poll.json();
  }
  if (!prediction.output) throw new Error(`Timed out generating ${entry.filename}.`);

  const audio = await fetch(prediction.output);
  if (!audio.ok) throw new Error(`Could not download ${entry.filename}.`);
  const bytes = Buffer.from(await audio.arrayBuffer());
  if (bytes.subarray(0, 4).toString('ascii') !== 'RIFF') {
    throw new Error(`${entry.filename} was not returned as WAV audio.`);
  }
  await writeFile(destination, bytes);
  console.log(`generated ${entry.filename}`);
}

const queue = [...entries];
async function worker() {
  while (queue.length > 0) await createClip(queue.shift());
}
await Promise.all([worker(), worker()]);

const manifest = `// Generated by scripts/generate-demo-audio.mjs.\n` +
  `const clips: Record<string, string> = ${JSON.stringify(Object.fromEntries(entries.map(({ key, filename }) => [key, filename])), null, 2)};\n\n` +
  `export function demoVoiceClipUrl(key: string): string | null {\n` +
  `  const filename = clips[key];\n` +
  `  return filename ? \`${'${import.meta.env.BASE_URL}'}audio/demos/${'${filename}'}\` : null;\n` +
  `}\n`;
await writeFile(manifestPath, manifest);
console.log(`wrote ${entries.length} demo clips and ${path.relative(repo, manifestPath)}`);
